namespace ConsoleAppAula05
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Laço de Repetição - For

            // escreva um algoritmo que mostre todos os números de 0 a 10.

            for (int i = 0; i <= 10; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}